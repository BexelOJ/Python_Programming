n = int(input())
while n > 0:
    print(n, end=' ')
    n -= 1
print()

while (n <=6):
    print(n, end=' ')
    n += 1
    if(n==6):
        break
print() 
# The above code is a simple while loop that prints numbers from n down to 1.
# It starts with an integer input n and continues to print the value of n while decrementing it by 1 in each iteration.
# The loop terminates when n becomes 0 or negative.


