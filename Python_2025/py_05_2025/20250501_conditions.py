n = int(input("Enter a Number: "))
if n<10:    
    print(f"{n} is less than 10")
else:
    print(f"{n} is greater than 10")

if n%2 == 0:
    print(f"{n} is even")
else:
    print(f"{n} is odd")
    
if n%3 == 0:
    print(f"{n} is divisible by 3")
else:
    print(f"{n} is not divisible by 3")

    